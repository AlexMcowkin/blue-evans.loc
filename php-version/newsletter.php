</p><a href="#top"><img src="http://www.cpasitesolutions.com/content/newsletter/images/all_botnav_arrow_off.png" alt="Go to top" border="0" align="right"></a><br><br><a name="2"></a><h2 class="hubtab vertgradient">Hobby or Business? Why It Matters</h2><div class="cellcolor headerline"></div><p><p>Millions of Americans have hobbies such as sewing, woodworking, fishing, gardening, stamp and coin collecting, but when that hobby starts to turn a profit, it might just be considered a business by the IRS.</p>

<h3>Definition of a Hobby vs. a Business</h3>
<p>The IRS defines a hobby as an activity that is not pursued for profit.  A business, on the other hand, is an activity that is carried out with the reasonable expectation of earning a profit.</p> 

<p>The tax considerations are different for each activity so it's important for taxpayers to determine whether an activity is engaged in for profit as a business or is just a hobby for personal enjoyment.</p>

<p>Of course, you must report and pay tax on income from almost all sources, including hobbies.  But when it comes to deductions such as expenses and losses, the two activities differ in their tax implications.</p>

<h3>Is Your Hobby Actually a Business?</h3>

<p>If you're not sure whether you're running a business or simply enjoying a hobby, here are nine factors you should consider:</p>

<ul>
<li><p>Whether you carry on the activity in a businesslike manner.</p></li>
<li><p>Whether the time and effort you put into the activity indicate you intend to make it profitable.</p></li>
<li><p>Whether you depend on income from the activity for your livelihood.</p></li>
<li><p>Whether your losses are due to circumstances beyond your control (or are normal in the startup phase of your type of business).</p></li>
<li><p>Whether you change your methods of operation in an attempt to improve profitability.
</p></li>
<li><p>Whether you, or your advisors, have the knowledge needed to carry on the activity as a successful business.</p></li>
<li><p>Whether you were successful in making a profit in similar activities in the past.</p></li>
<li><p>Whether the activity makes a profit in some years, and how much profit it makes.</p></li>
<li><p>Whether you can expect to make a future profit from the appreciation of the assets used in the activity.
</p></li>
</ul>

<p>An activity is presumed to be for profit if it makes a profit in at least three of the last five tax years, including the current year (or at least two of the last seven years for activities that consist primarily of breeding, showing, training, or racing horses).</p>

<p>The IRS says that it looks at all facts when determining whether a hobby is for pleasure or business, but the profit test is the primary one.  If the activity earned income in three out of the last five years, it is for profit.  If the activity does not meet the profit test, the IRS will take an individualized look at the facts of your activity using the list of questions above to determine whether it's a business or a hobby.  (It should be noted that this list is not all-inclusive.)</p>

<p><b>Business Activity:</b> If the activity is determined to be a business, you can deduct ordinary and necessary expenses for the operation of the business on a Schedule C or C-EZ on your Form 1040 without considerations for percentage limitations. An ordinary expense is one that is common and accepted in your trade or business. A necessary expense is one that is appropriate for your business.</p>

<p><b>Hobby:</b> If an activity is a hobby, not for profit, losses from that activity may not be used to offset other income. You can only deduct expenses up to the amount of income earned from the hobby.  These expenses, with other miscellaneous expenses, are itemized on Schedule A and must also meet the 2 percent limitation of your adjusted gross income in order to be deducted. </p>

<h3>What Are Allowable Hobby Deductions?</h3>

<p>If your activity is not carried on for profit, allowable deductions cannot exceed the gross receipts for the activity.</p>

<blockquote class="note"><p><b>Note:</b> Internal Revenue Code Section 183 (Activities Not Engaged in for Profit) limits deductions that can be claimed when an activity is not engaged in for profit. IRC 183 is sometimes referred to as the "hobby loss rule."</p></blockquote>

<p>Deductions for hobby activities are claimed as itemized deductions on Schedule A, Form 1040. These deductions must be taken in the following order and only to the extent stated in each of three categories:</p>

<ul>
<li><p>Deductions that a taxpayer may claim for certain personal expenses, such as home mortgage interest and taxes, may be taken in full.</p></li>
<li><p>Deductions that don't result in an adjustment to the basis of property, such as advertising, insurance premiums, and wages, may be taken next, to the extent gross income for the activity is more than the deductions from the first category.</p></li>

<li><p>Deductions that reduce the basis of property, such as depreciation and amortization, are taken last, but only to the extent gross income for the activity is more than the deductions taken in the first two categories.</p></li>
</ul>

<p>If your hobby is regularly generating income, it could make tax sense for you to consider it a business because you might be able to lower your taxes and take certain deductions.</p>
