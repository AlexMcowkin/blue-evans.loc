<h3>Self-Employed</h3>

<p>If you run an income-generating business with no employees, then you're considered self-employed. You can get coverage through the Healthcare Marketplace and use it to find coverage that fits your needs.</p>


<p><blockquote><b>Note:</b> You are still considered self-employed even if you hire independent contractors to do work for you.</blockquote></p>


<p>If you currently have individual insurance--a plan you bought yourself and not the kind you get through an employer--you may be able to change to a Marketplace plan.</p>


<p><blockquote><b>Note:</b> You can't be denied coverage or charged more because you have a pre-existing health condition.</blockquote></p>


<h3>Small Businesses (50 or Fewer Employees)</h3>

<p>If you have 50 or fewer full-time equivalent (FTE) employees (generally, workers whose income you report on a W-2 at the end of the year) you are considered a small business under the health care law.</p>


<p>As a small business, you may get insurance for yourself and your employees through the SHOP (Small Business Health Options Programs) Marketplace. This applies to non-profit organizations as well.</p>

<p><blockquote><b>Note:</b> Beginning in 2016, the SHOP Marketplace will be open to employers with 100 or fewer FTEs.</blockquote></p>


<p>As an employer, you must provide notification to your employees of coverage options available through the Marketplace and are required to provide this notice to all current employees and to each new employee regardless of plan enrollment status or full or part-time employment. The Department of Labor has sample notices that employers can use to comply with this regulation. One notice is for employers who do not offer a health care plan and the second for employers who offer a health care plan.</p>

<p>If you have fewer than 25 employees, you may qualify for the Small Business Tax Credit (see next section). Non-profit organizations may be eligible for the tax credit as well.</p>

<h3>Small Business Health Care Tax Credit</h3>

<p>Small businesses and tax-exempt organization that employ 25 or fewer, full-time equivalent workers with average incomes of $50,000 or less, and that pay at least half (50 percent) of the premiums for employee health insurance coverage are eligible for the Small Business Health Care Tax Credit.</p>


<p>Starting in 2014, the tax credit is worth up to 50 percent of your contribution toward employees' premium costs (up to 3 percent for tax-exempt employers). The tax credit is highest for companies with fewer than 10 employees who are paid an average of $25,000 or less. The smaller the business, the bigger the credit is.</p>

<p>For tax years 2010 through 2013, the maximum credit was 35 percent for small business employers and 25 percent for small tax-exempt employers such as charities.</p>

<p>Businesses that have already filed and later find that they qualified in 2013 or an earlier year can still claim the credit by filing an amended return for the affected years. A three-year statute of limitations normally applies to these refund claims.</p>

<p><blockquote><b>Note:</b> The credit is available only if you get coverage through the SHOP Marketplace and is available to eligible employers for two consecutive taxable years.</blockquote></p>

<p>If you are a small business employer who did not owe tax during the year, you can carry the credit back or forward to other tax years. In addition, because the amount of the health insurance premium payments is more than the total credit, eligible small businesses can still claim a business expense deduction for the premiums in excess of the credit--in other words, both a credit and a deduction for employee premium payments.</p>
 
<p>The credit is refundable for small tax-exempt employers as well, so even if you have no taxable income, you may be eligible to receive the credit as a refund as long as it does not exceed your income tax withholding and Medicare tax liability. </p>

<h3>Additional Tax on Businesses Not Offering Minimum Essential Coverage</h3>

<p>Effective January 1, 2015 an additional tax will be levied on businesses with 100 or more full-time equivalent (FTE) employees that do not offer minimum essential coverage. However,   it will not apply until 2016 to employers with at least 50 but fewer than 100 full-time employees.</p>

<p>Employers subject to the employer responsibility provisions in 2015 must 
offer coverage to at least 70 percent of full-time employees as one of the conditions for avoiding an assessable payment, rather than 95 percent which begins in 2016.
</p>

<p>This penalty is sometimes referred to as the Employer Shared Responsibility Payment.</p>


<p>You may have to pay this additional tax if you have 50 or more full-time equivalent employees and at least one of your full-time employees gets lower costs on their monthly premiums through the Marketplace.</p>

<p><blockquote><b>Note:</b> Employers with fewer than 50 FTE employees are considered small businesses and are exempt from the additional tax.</blockquote></p>

<p>The amount of the annual Employer Shared Responsibility Payment is based partly on whether you offer insurance.</p>

<ul>
<li>If you don't offer insurance, the annual payment is $2000 per full-time employee (excluding the first 30 employees for business with 50-99 full time employees and excluding the first 80 employees for those with 100 or more full time employees)</li>

</ul>

<p><blockquote><b>Note:</b> For purposes of this calculation, a full-time employee does not include a full-time equivalent. </blockquote></p>

<ul>

<li>If you do offer insurance, but the insurance doesn't meet the minimum requirements, the annual payment is the lesser of $3000 per full-time employee who qualifies for premium savings in the Marketplace or $2,000 per full-time employee (minus 30 full-time employees).</li>

</ul>

<p><blockquote><b>Note:</b> Unlike employer contributions to employee premiums, the Employer Shared Responsibility Payment is not tax deductible.</blockquote></p>


<p>A health plan meets minimum value if it covers at least 60 percent of the total allowed costs of benefits provided under the plan. To determine whether other coverage meets minimum value, please contact us for assistance.</p>

<p><blockquote><b>Note:</b> All plans in the Marketplace meet minimum value, so any coverage offered through the SHOP Marketplace should qualify.</blockquote></p>

<h3>Excise Tax on High Cost Employer-Sponsored Insurance</h3>

<p>Effective in 2018, a 40 percent excise tax indexed for inflation will be imposed on employers with insurance plans where the annual premium exceeds $10,200 (individual) or $27,500 (family). For retirees age 55 and older, the premium levels are higher, $11,850 for individuals and $30,950 for families.</p>


<h3>Excise Tax on Medical Devices</h3>

<p>Effective January 1, 2014, a 2.3 percent tax will be levied on manufacturers and importers on the sale of certain medical devices.</p>

<h3>Indoor Tanning Services</h3>

