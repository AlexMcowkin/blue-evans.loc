<p> Additionally, if you meet the requirements of the tax law for the deduction of moving expenses, you can deduct allowable expenses for a move to the area of a new main job location within the United States or its possessions. Your move may be from one United States location to another or from a foreign country to the United States.</p>

<blockquote><p><b>Note:</b> The rules applicable to moving within or to the United States are different from the rules that apply to moves outside the United States. These rules are discussed separately. </p>

</blockquote><p> To qualify for the moving expense deduction, you must satisfy three requirements.</p>

<p>Under the first requirement, your move must closely relate to the start of work. Generally, you can consider moving expenses within one year of the date you first report to work at a new job location. Additional rules apply to this requirement. Please contact us if you need assistance understanding this requirement.</p>

<p>The second requirement is the "distance test"; your new workplace must be at least 50 miles farther from your old home than your old job location was from your old home. For example, if your old main job location was 12 miles from your former home, your new main job location must be at least 62 miles from that former home. If you had no previous workplace, your new job location must be at least 50 miles from your old home.</p>

<p>The third requirement is the "time test." If you are an employee, you must work full-time for at least 39 weeks during the first 12 months immediately following your arrival in the general area of your new job location. </p>

<p>If you are self-employed, you must work full time for at least 39 weeks during the first 12 months and for a total of at least 78 weeks during the first 24 months immediately following your arrival in the general area of your new work location. </p>

<p>There are exceptions to the time test in case of death, disability and involuntary separation, among other things. </p>

<p>If your income tax return is due before you have satisfied this requirement, you can still deduct your allowable moving expenses if you expect to meet the time test.</p>

<blockquote><b><p>Note:</b> If you are a member of the armed forces and your move was due to a military order and permanent change of station, you do not have to satisfy the "distance or time tests". </p></blockquote>

<h3>What Are "Reasonable" Expenses?</h3>

<p>You can deduct only those expenses that are reasonable under the circumstances of your move. For example, the cost of traveling from your former home to your new one should be by the shortest, most direct route available by conventional transportation. If during your trip to your new home, you make side trips for sight-seeing, the additional expenses for your side trips are not deductible as moving expenses. And, you cannot deduct your travel meal costs.</p>

<p>You can deduct the cost of packing, crating and transporting your household goods and personal property, and you may be able to include the cost of storing and insuring these items while in transit. You may also deduct costs of connecting or disconnecting utilities.</p>

<blockquote><p><b>Tip:</b> You can include the cost of storing and insuring household goods and personal effects within <b>any period of 30 consecutive days</b> after the day your things are moved from your former home and before they are delivered to your new home.</p></blockquote>

<blockquote><p><b>Tip:</b> You can deduct the cost of shipping your car and your pets to your new home.</p></blockquote>

<p><b> Nondeductible expenses. </b> You cannot deduct as moving expenses any part of the purchase price of your new home, the costs of buying or selling a home, or the cost of entering into or breaking a lease. Don't hesitate to call us if you have any questions about which expenses are deductible.
 
<p><b> Reimbursed expenses.</b>  If, at a later date, your employer reimburses you for the costs of a move for which you took a deduction, you may have to include the reimbursement as income on your tax return. Report any taxable amount on your tax return in the year you get the payment.</p>
<h3>Travel Expenses - How to Calculate the Deduction</h3>
<p>If you use your car to take yourself, members of your household, or your personal effects to your new home, you can figure your expenses by deducting either:</p>
<ol>
	<li>Your <b>actual expenses</b>, such as gas and oil for your car, if you keep an accurate record of each expense, or</li>
	<li>The standard mileage rate is 23.5 cents per mile for miles driven during 2014. </li>
</ol>
<blockquote><p><b>Tip:</b> If you choose the standard mileage rate you can deduct parking fees and tolls you pay in moving. You cannot deduct any general repairs, general maintenance, insurance, or depreciation for your car.</p></blockquote>
<p>You can deduct the cost of transportation and lodging for yourself and members of your household while traveling from your former home to your new home. This includes expenses for the day you arrive. You can include any lodging expenses you had in the area of your former home within one day after you could not live in your former home because your furniture had been moved. You can deduct expenses for only one trip to your new home for yourself and members of your household; however, all of you do not have to travel together. </p>
<h3>Member of Your Household</h3>
<p>You can deduct moving expenses you pay for yourself and members of your household. A member of your household is anyone who has both your former and new home as his or her home. It does not include a tenant or employee, unless you can claim that person as a dependent.</p>

<h3>Reporting Address Changes</h3>

<p>When you move, be sure to update your address with the U.S. Post Office as well as the IRS. Use Form 8822, Change of address, when notifying the IRS of an address change.</p>

<p>In addition, if you purchased health insurance coverage from the Health Insurance Marketplace, you may receive advance payment of the premium tax credit in 2014. It is important that you report changes in circumstances, such as when you move to a new address, to your Marketplace. </p>
 
