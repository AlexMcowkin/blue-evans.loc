<p><p>Whether you play the lottery, roll the dice, play cards, or bet on the ponies, all of your gambling winnings are taxable and must be reported on your tax return. If you're a casual gambler, here's what you need to know about figuring gambling income and loss.  </p>
<p>1. Gambling income includes winnings from lotteries, raffles, horse races and casinos. It also includes cash and the fair market value of prizes you receive, such as cars and trips. </p>

<p>2. If you win, you may receive a Form W-2G, Certain Gambling Winnings, from the payer. The form reports the amount of your winnings to you and the IRS. The payer issues the form depending on the type of game you played, the amount of winnings, and other factors. You'll also receive a Form W-2G if the payer withholds federal income tax from your winnings. </p>
<p>3. You must report all your gambling winnings as income on your federal income tax return. This is true even if you do not receive a Form W-2G. </p>
<p>4. If you're a casual gambler, report your winnings on the "Other Income" line of your Form 1040, U. S. Individual Income Tax Return. </p>
<p>5. You may deduct your gambling losses on Schedule A, Itemized Deductions. The deduction is limited to the amount of your winnings. You must report your winnings as income and claim your allowable losses separately. You cannot reduce your winnings by your losses and report the difference. </p>
<p>6. You must keep accurate records of your gambling activity. This includes items such as receipts, tickets or statements. You should also keep a diary or log of your gambling activity. Your records should show your winnings separately from your losses. </p>

